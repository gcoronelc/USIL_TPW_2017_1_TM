package pe.egcc.colegio.model;

public class Estudiante {

    private String IDEST;
    private String APELLIDO;
    private String NOMBRE;
    private String DNI;
    private String DIRECCION;
    private String CORREO;
    private String APONOMBRE;
    private String APOCORREO;
    private String APOTELEFONO;

    public Estudiante() {
    }

    public String getIDEST() {
        return IDEST;
    }

    public void setIDEST(String IDEST) {
        this.IDEST = IDEST;
    }

    public String getAPELLIDO() {
        return APELLIDO;
    }

    public void setAPELLIDO(String APELLIDO) {
        this.APELLIDO = APELLIDO;
    }

    public String getNOMBRE() {
        return NOMBRE;
    }

    public void setNOMBRE(String NOMBRE) {
        this.NOMBRE = NOMBRE;
    }

    public String getDNI() {
        return DNI;
    }

    public void setDNI(String DNI) {
        this.DNI = DNI;
    }

    public String getDIRECCION() {
        return DIRECCION;
    }

    public void setDIRECCION(String DIRECCION) {
        this.DIRECCION = DIRECCION;
    }

    public String getCORREO() {
        return CORREO;
    }

    public void setCORREO(String CORREO) {
        this.CORREO = CORREO;
    }

    public String getAPONOMBRE() {
        return APONOMBRE;
    }

    public void setAPONOMBRE(String APONOMBRE) {
        this.APONOMBRE = APONOMBRE;
    }

    public String getAPOCORREO() {
        return APOCORREO;
    }

    public void setAPOCORREO(String APOCORREO) {
        this.APOCORREO = APOCORREO;
    }

    public String getAPOTELEFONO() {
        return APOTELEFONO;
    }

    public void setAPOTELEFONO(String APOTELEFONO) {
        this.APOTELEFONO = APOTELEFONO;
    }

 
}
